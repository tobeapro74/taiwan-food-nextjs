// 구글 맵 링크 생성
export function getGoogleMapsLink(name: string, location: string): string {
  let query = name || "";
  if (location && !location.includes("야시장")) {
    query += " " + location;
  }
  query += " Taipei Taiwan";
  return `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(query.trim())}`;
}
